    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
     
        String id = jComboBox1.getSelectedItem().toString();
       try {  
               String nama_obat = txtnama_obat.getText();
               String katagori = txtkatagori.getText();
               String kode_obat = txtkode_obat.getText();
           
           pst = con.prepareStatement("update menu_obat set nama_obat=?, katagori=?, kode_obat=? where id=?");
            pst.setString(1, nama_obat);
               pst.setString(2, katagori);
               pst.setString(3, kode_obat);
               pst.setString(4, id);
               
               int k = pst.executeUpdate();
               
               if (k==1){
                   JOptionPane.showMessageDialog(this, "Data Berhasil Di Update");
                   txtnama_obat.setText("");
                   txtkatagori.setText("");
                   txtkode_obat.setText("");
                   txtnama_obat.requestFocus();
                   LoadobatID();
               
               }
               
               else{
                    JOptionPane.showMessageDialog(this, "Data Gagal Di Update");
               }

           
           
       } catch (SQLException ex) {
           Logger.getLogger(obat.class.getName()).log(Level.SEVERE, null, ex);
       }
        
        
        
    }//GEN-LAST:event_jButton3ActionPerformed

