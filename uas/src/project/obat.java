    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        
        try {
            String nama_obat = txtnama_obat.getText();
            String katagori = txtkatagori.getText();
            String kode_obat = txtkode_obat.getText();
            
            pst = con.prepareStatement("insert into menu_obat (nama_obat, katagori, kode_obat)values (?,?,?)");
             pst.setString(1, nama_obat);
               pst.setString(2, katagori);
               pst.setString(3, kode_obat);
          
               
               int k = pst.executeUpdate();
               
               if (k==1){
                   JOptionPane.showMessageDialog(this, "Data Berhasil Ditambahkan");
                   txtnama_obat.setText("");
                   txtkatagori.setText("");
                   txtkode_obat.setText("");
                   txtnama_obat.requestFocus();
               }
               
               else{
                    JOptionPane.showMessageDialog(this, "Data Gagal Ditambahkan");
               }
            
            
        } catch (SQLException ex) {
            Logger.getLogger(obat.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
        
    }//GEN-LAST:event_jButton2ActionPerformed

